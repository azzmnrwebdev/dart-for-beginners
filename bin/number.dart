void main() {
  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  /**
   *  num adalah tipe data yang dapat merubah
   *  dari int ke double, sebalik nya dari
   *  double ke int, contoh nya:
   */

  // dari int ke double
  num number = 20;
  print(number);

  number = 20.5;
  print(number);

  // dari double ke int
  num number3 = 30.5;
  print(number3);

  number3 = 30;
  print(number3);
}